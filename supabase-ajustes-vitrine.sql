-- Mantem a RPC usada pelo frontend atual.
CREATE OR REPLACE FUNCTION public.increment_acessos(id_produto text)
RETURNS void
LANGUAGE plpgsql
SECURITY DEFINER
AS $$
BEGIN
  UPDATE public.vitrine
  SET acessos = COALESCE(acessos, 0) + 1
  WHERE id::text = id_produto;
END;
$$;
