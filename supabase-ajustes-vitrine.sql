-- Arquivo unico para alinhar o Supabase com o frontend atual.
-- Pode ser executado no SQL Editor do Supabase.

DO $$
DECLARE
  vitrine_id_type text;
BEGIN
  SELECT pg_catalog.format_type(a.atttypid, a.atttypmod)
  INTO vitrine_id_type
  FROM pg_attribute a
  JOIN pg_class c ON c.oid = a.attrelid
  JOIN pg_namespace n ON n.oid = c.relnamespace
  WHERE n.nspname = 'public'
    AND c.relname = 'vitrine'
    AND a.attname = 'id'
    AND a.attnum > 0
    AND NOT a.attisdropped;

  IF vitrine_id_type IS NULL THEN
    RAISE EXCEPTION 'Tabela public.vitrine ou coluna id nao encontrada';
  END IF;

  EXECUTE format($sql$
    CREATE TABLE IF NOT EXISTS public.vitrine_variacoes (
      id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
      vitrine_id %s NOT NULL REFERENCES public.vitrine(id) ON DELETE CASCADE,
      nome text NOT NULL,
      tipo_variacao text NOT NULL,
      ordem integer NOT NULL DEFAULT 0,
      created_at timestamptz NOT NULL DEFAULT now(),
      updated_at timestamptz NOT NULL DEFAULT now()
    )
  $sql$, vitrine_id_type);
END $$;

CREATE TABLE IF NOT EXISTS public.vitrine_precos (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  variacao_id bigint NOT NULL REFERENCES public.vitrine_variacoes(id) ON DELETE CASCADE,
  preco numeric(10,2) NOT NULL,
  ordem integer NOT NULL DEFAULT 0,
  created_at timestamptz NOT NULL DEFAULT now(),
  updated_at timestamptz NOT NULL DEFAULT now()
);

CREATE TABLE IF NOT EXISTS public.vitrine_precos_faixas (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  variacao_id bigint NOT NULL REFERENCES public.vitrine_variacoes(id) ON DELETE CASCADE,
  quantidade_minima integer NOT NULL,
  quantidade_label text NOT NULL,
  preco numeric(10,2) NOT NULL,
  destaque boolean NOT NULL DEFAULT false,
  ordem integer NOT NULL DEFAULT 0,
  created_at timestamptz NOT NULL DEFAULT now(),
  updated_at timestamptz NOT NULL DEFAULT now()
);

CREATE UNIQUE INDEX IF NOT EXISTS vitrine_variacoes_vitrine_nome_uidx
  ON public.vitrine_variacoes (vitrine_id, nome);

CREATE INDEX IF NOT EXISTS vitrine_variacoes_vitrine_ordem_idx
  ON public.vitrine_variacoes (vitrine_id, ordem);

CREATE UNIQUE INDEX IF NOT EXISTS vitrine_precos_variacao_ordem_uidx
  ON public.vitrine_precos (variacao_id, ordem);

CREATE UNIQUE INDEX IF NOT EXISTS vitrine_precos_faixas_variacao_qtd_uidx
  ON public.vitrine_precos_faixas (variacao_id, quantidade_minima);

CREATE INDEX IF NOT EXISTS vitrine_precos_faixas_variacao_ordem_idx
  ON public.vitrine_precos_faixas (variacao_id, ordem);

